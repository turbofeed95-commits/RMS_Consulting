export function LanguageProvider({ children }) {
  const [lang, setLang] = useState('pt')
  const toggle = () => setLang(l => l === 'en' ? 'pt' : 'en')
  const strings = t[lang]
  return (
    <LanguageContext.Provider value={{ lang, toggle, strings }}>
      {children}
    </LanguageContext.Provider>
  )
}
